<p><span style="color:#696969;"><strong>Dear All,</strong></span></p>
<p><span style="color:#800000;"><strong>The Conference „Food and nutrition in 21st century”</strong></span><span style="color: rgb(105, 105, 105);"> was successfully completed. It was attended by more than 450 participants from all over the Europe, representing the science communities, industry leaders, national and European S&amp;T policy bodies and civil society. During the debate major challenges to food security and safety as well as to nutrition and well being of European citizens were identified and the measures which shall be undertaken to mitigate them were proposed. The final report is now being prepared by Programme Committee and will be released within two months. In a meantime, you can enjoy the photographic documentation of the Conference, presented in the photo gallery. Shall you have some new ideas on priorities for food and nutrition science and technology, which you would like to share with the international community, you can present them at the discussion forum open at Conference’s website.<br /><br />The success of the Conference would not be possible without your active involvement. On behalf of the organizers, I would like to thank you for coming and for your great contribution.</span></p>
